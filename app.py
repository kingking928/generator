
import streamlit as st
import random

st.set_page_config(page_title="轻松一天生成器（上海版）", page_icon="🌿")

st.title("🌿 轻松一天生成器（上海版）")
st.write("选择你今天的状态，我帮你安排一个轻松的一天～")

# 用户选择
mood = st.selectbox(
    "你现在的状态是：",
    ["一个人", "想放松", "不想花钱", "随便逛逛"]
)

# 预设方案库
plans = {
    "一个人": [
        ["去公园散步", "找家咖啡店坐一会", "简单吃个晚餐"],
        ["逛书店", "找个安静角落发呆", "回家看电影"],
        ["沿街随便走走", "买杯饮料", "早点回家休息"]
    ],
    "想放松": [
        ["找个绿地晒太阳", "喝杯咖啡", "慢慢吃顿饭"],
        ["去安静的商场逛逛", "随便看看小店", "吃点甜品"],
        ["做一次城市walk", "找地方坐一会", "回家放松"]
    ],
    "不想花钱": [
        ["去公园走走", "找个公共空间坐着", "回家"],
        ["城市随便逛", "路边看看人", "早点休息"],
        ["图书馆待一会", "散步", "回家"]
    ],
    "随便逛逛": [
        ["随机坐一站地铁下车", "附近逛逛", "找家店吃点东西"],
        ["去一个没去过的街区", "随便走走", "喝点东西"],
        ["打开地图随机选点", "走过去", "路上随便看看"]
    ]
}

# 花费范围（简单估算）
cost_range = {
    "一个人": "约50-100元",
    "想放松": "约80-150元",
    "不想花钱": "0-30元",
    "随便逛逛": "约30-80元"
}

# 点击生成
if st.button("✨ 生成我的一天"):
    selected_plan = random.choice(plans[mood])

    st.subheader("🌈 今日推荐：")
    st.write(f"👉 状态：{mood}")

    st.markdown("---")

    st.write("🗓 行程安排：")
    st.write(f"- 下午：{selected_plan[0]}")
    st.write(f"- 傍晚：{selected_plan[1]}")
    st.write(f"- 晚上：{selected_plan[2]}")

    st.markdown("---")

    st.write(f"💰 预计花费：{cost_range[mood]}")

    # 氛围文案（随机一句）
    vibes = [
        "今天适合慢一点，不用着急。",
        "就这样随便走走，也挺好的。",
        "不用特别安排，轻松一点就好。",
        "给自己一点不被打扰的时间。"
    ]

    st.markdown("---")
    st.write("🌿 今日氛围：")
    st.write(random.choice(vibes))